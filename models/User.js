const mongoose = require('mongoose');

//生成Schema类，定义好数据类型
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

//生成Model类
module.exports = User = mongoose.model('user', UserSchema);
